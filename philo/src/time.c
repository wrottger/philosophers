#include "philosophers.h"

size_t	get_time(void)
{
	struct timeval	time;

	gettimeofday(&time, NULL);
	return (time.tv_sec * 1000 + time.tv_usec / 1000);
}

void	ft_wait(size_t time)
{
	size_t	start_time;

	start_time = get_time();
	while (get_time() - start_time < time)
		usleep(100);
}
